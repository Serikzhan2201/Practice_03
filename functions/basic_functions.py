# Example 1: A function with no parameters - prints a greeting
def say_hello():
    print("Hello!")
say_hello()

# Example 2: A function with one parameter - greets a person by name
def greet(name):
    print(f"Hi, {name}!")
greet("Alice")

# Example 3: A function that adds two numbers
def add(a, b):
    result = a + b
    print(f"{a} + {b} = {result}")
add(3, 5)

# Example 4: A function that multiplies two numbers
def multiply(x, y):
    print(x * y)
multiply(4, 6)
